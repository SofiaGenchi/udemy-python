# METODO DE CADENA DE CARACTERES

a = 'Hola Mundo'

mayuscula = a.upper()
print(mayuscula)

minuscula = a.lower()
print(minuscula)

primer_mayuscula = a.capitalize()
print(primer_mayuscula)

titulo = a.title()
print(titulo)

contar = a.count('o')
print(contar)

reemplazar = a.replace('H', 'S')
print(reemplazar)


espacios = '    Hola    Mundo'
borrar_espacios = espacios.strip()
print(borrar_espacios)